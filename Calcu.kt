package Fernanda.Hernandez.poofernanda

class Calcu {

    fun suma(num1: Int, num2: Int): Int{
        val resultado = num1+num2

        return resultado
    }
    fun multiplicar(num1: Int, num2: Int): Int{
        val resultado = num1*num2

        return resultado
    }
    fun resta(num1: Int, num2: Int): Int{
        val resultado = num1-num2

        return resultado
    }
    fun dividir(num1: Int, num2: Int): Int{
        val resultado = num1/num2

        return resultado
    }
}